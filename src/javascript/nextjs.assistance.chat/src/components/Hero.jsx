function useMoveUp() {
  const y = useMotionValue(0)
  const x = useMotionValue(0)
  const [isMoving, setIsMoving] = useState(false)

  const ref = useRef(null)

  const moveUp = () => {
    setIsMoving(true)
    y.set(-50)
    x.set(0)
  }

  const moveDown = () => {
    setIsMoving(false)
    y.set(0)
    x.set(0)
  }

  return {
    ref,
    isMoving,
    moveUp,
    moveDown,
    x,
    y,
  }
}

